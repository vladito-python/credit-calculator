# El símbolo # sirve para agregar comentarios en Python.

# Python es un lenguaje de alto nivel, es decir, que no requiere de sintaxis extensas para decir una cosa, es fácil de comprender y manejar.

# La función print muestra datos, valores o resultados de variables dentro de ():

print("Hola mundo")

print('Hola mundo') # También se puede usar comillas simples.

print("Hola", "Mundo", "Python", 2025) # La coma sirve para separar valores, se muestra un espacio por defecto entre cada valor. Funciona como una forma simple de unir elementos sin concatenar.

print("Hola", "Mundo", "Python", sep="-") # El parámetro sep reemplaza el espacio entre cada valor por un guión.

print("Hola " *3) # Imprime Hola tres veces.

print("Hola\nMundo") # \n hace un salto de línea entre Hola y Mundo. Sólo se usa en cadenas de texto.

print("\n") # Se puede imprimir para hacer un salto de línea.
