# Las variables son cajas que almacenan valores modificables:

nombre = "Gerson" # nombre es la caja y "Gerson" es el valor.
print(nombre)

edad_años = 32
print(edad_años)

altura_m = 1.66
print(altura_m)

# El valor "Gerson" de la variable "nombre" puede ser modificado, al igual que la edad y la altura.
# Una variable no admite espacios, se escribe: edad_años o altura_m en lugar de: edad años o altura m.
# Una variable no admite números al inicio como 1nombre.
# Tampoco es recomendable usar tíldes o ñ.
# Una variable jamás puede llamarse print ya que print es una palabra reservada por Python. Las palabras reservadas de Python son:
# False      await      else       import     pass
# None       break      except     in         raise
# True       class      finally    is         return
# and        continue   for        lambda     try
# as         def        from       nonlocal   while
# assert     del        global     not        with
# async      elif       if         or         yield

name = "Gerson"  # La variable "name" almacena un valor tipo texto
name = 32  # La misma variable ahora almacena un valor tipo número

