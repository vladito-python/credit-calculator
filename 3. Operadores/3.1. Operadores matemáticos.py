# Operadores matemáticos:

a = 20
b = 6
print ("El resultado es:" , (a + b)) # El signo + es para suma.
print("El resultado es:" , (a - b)) # El signo - es para resta.
print("El resultado es:" , (a * b))  # El signo * es para multiplicación.
print("El resultado es:" , (a / b)) # El signo / es para división.
print("El resultado es:" , (a ** b)) # Los signos ** son para potencia.
print("El resultado es:" , (a // b)) # Los signos // devuelven un valor entero de una división.
print("El resultado es:" , (a % b)) # El signo % o módulo devuelve el sobrante o residuo de una división. Aquí sobra 2 después de hacer la división, ya que 6 * 3 = 18.

