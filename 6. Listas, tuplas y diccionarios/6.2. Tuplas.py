# Las tuplas son colecciones de datos ordenados:

colores = ("Amarillo", "Azul", "Rojo")
print(colores)

print("\n")

# Para imprimir un elemento específico según su índice o posición en la tupla:

print(colores[0]) # Amarillo se posiciona en 0

print("\n")

# Las tuplas son inmutables, es decir que no se puede añadir, modificar ni eliminar sus datos. Son útiles para datos que no cambian.

dimensiones = (36, 89)
print(f"Ancho: {dimensiones[0]}, Alto: {dimensiones[1]}")

print("\n")